/* 以下 24x24 中文点阵用于 LCD 首屏演示，后续完整中文字库应独立设计。 */
const uint8_t *APP_Font_FindAscii5x7(char ch)
{
    unsigned int i;

    if (ch >= 'a' && ch <= 'z')
    {
        ch = (char)(ch - 'a' + 'A');
    }

    for (i = 0; i < sizeof(app_ascii_5x7) / sizeof(app_ascii_5x7[0]); i++)
    {
        if (app_ascii_5x7[i].ch == ch)
        {
            return app_ascii_5x7[i].rows;
        }
    }

    return app_ascii_5x7[0].rows;
}
